package com.String;

public class Shuxuelei {
	public static void main(String[] args) {
		System.out.println("计算绝对值得结果："+Math.abs(-1));
		System.out.println("计算正弦得结果："+Math.sin(1.57));
		System.out.println("计算余弦得结果："+Math.cos(2.0));
		System.out.println("计算正切得结果："+Math.tan(0.8));
		System.out.println("计算平方根得结果："+Math.sqrt(4));
		System.out.println("计算立方根得结果："+Math.cbrt(9));
		System.out.println("计算乘方得结果："+Math.pow(2,2));
		
		System.out.println("求大于参数的最小整数："+Math.ceil(4.6));
		System.out.println("求小于参数的最大整数："+Math.floor(-5.1));
		System.out.println("对小数进行四舍五入后的结果："+Math.round(-3.4));
		System.out.println("求两个数的较大值："+Math.max(6.1,6.3));
		System.out.println("求两个数的较小值："+Math.min(6.2,6.6));
		System.out.println("生成一个大于等于0.0小于1.0的随机值："+Math.random());
	}
}
